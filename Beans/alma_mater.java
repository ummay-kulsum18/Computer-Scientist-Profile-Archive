public class alma_mater {
    private String name;
    private String dob;
    private String a_institution;
    private String degree;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getA_institution() {
        return a_institution;
    }

    public void setA_institution(String a_institution) {
        this.a_institution = a_institution;
    }

    public String getDegree() {
        return degree;
    }

    public void setDegree(String degree) {
        this.degree = degree;
    }
}
